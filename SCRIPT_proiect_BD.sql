//CREAREA TABELEI RUTE
CREATE TABLE RUTE(ID_RUTA NUMBER(6) CONSTRAINT ID_RUTA_PK PRIMARY KEY, DURATA NUMBER(4) NOT NULL, 
NR_KM NUMBER(4) NOT NULL, PLECARE VARCHAR2(50) NOT NULL, SOSIRE VARCHAR2(50)); 

//CREAREA TABELEI PASAGERI
CREATE TABLE PASAGERI(ID_PASAGER NUMBER(6) CONSTRAINT ID_PASAGER_PK PRIMARY KEY, NUME VARCHAR2(30) NOT NULL,
PRENUME VARCHAR2(30) NOT NULL, TELEFON VARCHAR2(10) NOT NULL, EMAIL VARCHAR2(50) NOT NULL, TIP_BILET VARCHAR2(30) NOT NULL,
ID_RUTA NUMBER(6) NOT NULL, CONSTRAINT ID_RUTA_FK FOREIGN KEY (ID_RUTA) REFERENCES RUTE(ID_RUTA));

//CREAREA TABELEI BILETE
CREATE TABLE BILETE(ID_BILET NUMBER(6) CONSTRAINT ID_BILET_PK PRIMARY KEY, TIP VARCHAR2(15) NOT NULL, LOC NUMBER(3) NOT NULL, VAGON NUMBER(2) NOT NULL,
DATA_EMITERII DATE NOT NULL, DATA_CALATORIEI DATE NOT NULL, 
PRET NUMBER(8,2) NOT NULL, ID_RUTA NUMBER(6), ID_PASAGER NUMBER(6), CONSTRAINT ID_RUTA_BILET_FK FOREIGN KEY (ID_RUTA) REFERENCES RUTE(ID_RUTA),
CONSTRAINT ID_PASAGER_FK FOREIGN KEY (ID_PASAGER) REFERENCES PASAGERI(ID_PASAGER), CONSTRAINT DATA_CALATORIEI_CK CHECK (DATA_CALATORIEI <= DATA_EMITERII + INTERVAL '30' DAY));

//CREAREA TABELEI TRENURI
CREATE TABLE TRENURI(ID_TREN NUMBER(6) CONSTRAINT ID_TREN_PK PRIMARY KEY, TIP_TREN VARCHAR2(20) NOT NULL, NR_VAGOANE NUMBER(2) NOT NULL, NR_PASAGERI NUMBER(3) NOT NULL,
ID_RUTA NUMBER(6) NOT NULL, CONSTRAINT ID_RUTA_TREN_FK FOREIGN KEY (ID_RUTA) REFERENCES RUTE(ID_RUTA));

//CREAREA TABELEI VAGOANE
CREATE TABLE VAGOANE(ID_VAGON NUMBER(6) CONSTRAINT ID_VAGON_PK PRIMARY KEY, TIP_VAGON VARCHAR2(30) NOT NULL, CAPACITATE NUMBER(3) NOT NULL,
ID_TREN NUMBER(6) NOT NULL, CONSTRAINT ID_TREN_VAGON_FK FOREIGN KEY (ID_TREN) REFERENCES TRENURI(ID_TREN));

//CREAREA TABELEI PERSONAL_TREN
CREATE TABLE PERSONAL_TREN(ID_ANGAJAT NUMBER(6) CONSTRAINT ID_ANGAJAT_PK PRIMARY KEY, NUME VARCHAR2(30) NOT NULL, PRENUME VARCHAR2(30) NOT NULL, 
FUNCTIE VARCHAR2(30) NOT NULL, SALARIUL NUMBER(8,2) NOT NULL, DATA_ANGAJARII DATE NOT NULL, ID_TREN NUMBER(6) NOT NULL,
CONSTRAINT ID_TREN_ANGAJAT_FK FOREIGN KEY (ID_TREN) REFERENCES TRENURI(ID_TREN));

//CREAREA TABELEI REGIONALA
CREATE TABLE REGIONALA (ID_REGIONALA NUMBER(6) NOT NULL, ORAS_SEDIU VARCHAR2(50) NOT NULL, CONSTRAINT PK_REGIONALA PRIMARY KEY (ID_REGIONALA));

//CREAREA TABELEI STATII
CREATE TABLE STATII (ID_STATIE NUMBER(6) NOT NULL, NUME_STATIE VARCHAR2(50) NOT NULL, ORAS VARCHAR2(50) NOT NULL, TIP_STATIE VARCHAR2(20) NOT NULL, ID_REGIONALA NUMBER(6), CONSTRAINT PK_STATII PRIMARY KEY (ID_STATIE), CONSTRAINT FK_STATII_REGIONALA FOREIGN KEY (ID_REGIONALA) REFERENCES REGIONALA(ID_REGIONALA), CONSTRAINT CK_TIP_STATIE CHECK (TIP_STATIE IN ('Joncțiune', 'Intermediară', 'Terminus')));

//CREARE TABELEI STATII_RUTE
