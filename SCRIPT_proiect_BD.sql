    CONSTRAINT FK_SR_RUTA FOREIGN KEY (ID_RUTA) REFERENCES RUTE(ID_RUTA), CONSTRAINT FK_SR_STATIE FOREIGN KEY (ID_STATIE) REFERENCES STATII(ID_STATIE),
